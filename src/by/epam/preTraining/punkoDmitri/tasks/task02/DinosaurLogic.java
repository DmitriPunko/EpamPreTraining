package by.epam.preTraining.punkoDmitri.tasks.task02;

public class DinosaurLogic {
    public static double getDinosaurMassInMilligrams(double mass) {
        return mass * 1000000;
    }

    public static double getDinosaurMassInGrams(double mass) {
        return mass * 1000;
    }

    public static double getDinosaurMassInTons(double mass) {
        return mass / 1000;
    }
}
